# ----------------------------------------------------------------
# 핵심 파라미터
# ----------------------------------------------------------------
K = 0.5
# 변동성 계수
# 낮을수록 (0.3): 진입 빠름, 매매 횟수 많음, 리스크 높음
# 높을수록 (0.7): 진입 늦음, 매매 횟수 적음, 리스크 낮음
# 권장 범위: 0.3 ~ 0.7

INVEST_RATIO = 0.5
# 예수금 중 투자 비율
# 0.5 = 예수금의 50%만 투자
# 권장 범위: 0.3 ~ 0.8

# ----------------------------------------------------------------
# 익절 / 손절
# ----------------------------------------------------------------
PROFIT_RATE = 0.03
# 익절 기준 수익률 (USE_TRAILING_STOP = True 이면 무시됨)
# 0.03 = 매수가 대비 +3% 시 익절
# 공격적: 0.05 / 안정적: 0.02

LOSS_RATE = 0.02
# 손절 기준 손실률 (하드 스탑 — 트레일링 스탑과 무관하게 항상 작동)
# 0.02 = 매수가 대비 -2% 시 손절
# 공격적: 0.03 / 안정적: 0.01

# ----------------------------------------------------------------
# 트레일링 스탑
# ----------------------------------------------------------------
USE_TRAILING_STOP = True
# True  : 트레일링 스탑 사용 (고정 익절 대신 고점 추적 익절)
# False : 기존 고정 익절/손절 방식 유지

TRAILING_STOP_RATE = 0.02
# 고점 대비 하락률 트리거
# 0.02 = 고점 대비 -2% 하락 시 매도
# 공격적 (수익 극대화): 0.03 ~ 0.05
# 안정적 (수익 보전): 0.01 ~ 0.02

TRAILING_STOP_ACTIVATE_RATE = 0.01
# 트레일링 스탑 활성화 최소 수익률
# 0.01 = 매수가 대비 +1% 이상 상승해야 트레일링 스탑 작동
# 0으로 설정하면 매수 즉시 활성화

# ----------------------------------------------------------------
# 시간 설정
# ----------------------------------------------------------------
MARKET_OPEN     = "09:00"   # 장 시작 시간
MARKET_CLOSE    = "15:30"   # 장 종료 시간
FORCE_SELL_TIME = "15:20"   # 강제 청산 시간 (장 마감 10분 전)
CHECK_INTERVAL  = 10        # 시세 체크 주기 (초)
# 너무 짧으면 API 호출 제한에 걸릴 수 있음 (최소 1초 권장)

# ----------------------------------------------------------------
# 스크리닝 종목 풀
# ----------------------------------------------------------------
KOSPI_POOL_SIZE = 100
# 코스피200 목록에서 실제로 스크리닝할 종목 수 (1 ~ 200)
# 관심종목(watchlist.py)은 이 숫자와 무관하게 항상 전부 포함
# 50개 ≈ 25초 / 100개 ≈ 50초 / 200개 ≈ 100초

# ----------------------------------------------------------------
# 다종목 순차 전환 설정
# ----------------------------------------------------------------
MAX_TRADES_PER_DAY = 5
# 하루 최대 매매 횟수
# 스크리닝 결과가 많아도 이 횟수까지만 매매

RESCREEN_WAIT = 40
# 후보가 없을 때 재스크리닝까지 대기 시간 (초)
# 스크리닝 결과 조건 충족 종목이 없으면 이 시간 후 다시 스크리닝

SAME_STOCK_COOLDOWN = 180
# 같은 종목 재매매 쿨다운 (초)
# 한 종목 청산 후 이 시간 동안은 같은 종목 재진입 금지 (무한 재매매 방지)
# 0으로 설정하면 쿨다운 없음 (청산 즉시 재매매 가능)

# ----------------------------------------------------------------
# 돌파 직후 진입 안전장치
# ----------------------------------------------------------------
MAX_BREAKOUT_GAP = 1.0
# 돌파여유율 상한선 (%)
# 즉시 진입 가능한 최대 여유율. 이 이내면 바로 진입.
# 낮을수록 (0.5): 돌파 직후만 진입, 기회 적지만 안전
# 높을수록 (3.0): 다소 늦어도 진입, 기회 많지만 고점 위험
# 권장 범위: 0.5 ~ 2.0

REENTRY_BAND = 1.0
# 재진입 허용 밴드 (%)
# 고점 보류 후, 가격이 목표가 ~ 목표가×(1+이값%) 구간으로 내려오면 재진입
# 권장: MAX_BREAKOUT_GAP과 동일하게 설정

DROP_BUFFER = 0.3
# 돌파 실패 판정 완충구간 (%)
# 목표가 × (1 - 이값%) 밑으로 떨어져야 비로소 "돌파 실패"로 포기
# 낮을수록 (0.1): 칼같이 포기 / 높을수록 (1.0): 관대하게 대기
# 권장 범위: 0.2 ~ 0.5

# ----------------------------------------------------------------
# 동적 종목 리스트 (스크리닝 결과로 자동 업데이트)
# 절대 직접 수정하지 마세요 — screener.py가 자동으로 채웁니다
# ----------------------------------------------------------------
STOCK_CANDIDATES = []

def update_candidates(screened_list):
    """스크리닝 결과로 후보 종목 업데이트"""
    global STOCK_CANDIDATES
    STOCK_CANDIDATES = screened_list[:MAX_TRADES_PER_DAY]
    print(f"  후보 종목 {len(STOCK_CANDIDATES)}개 업데이트 완료")

# ----------------------------------------------------------------
# 매매 조건 요약 출력
# ----------------------------------------------------------------
def print_config():
    print("=" * 40)
    print(f"  K 값        : {K}")
    print(f"  투자 비율   : {int(INVEST_RATIO * 100)}%")
    if USE_TRAILING_STOP:
        print(f"  청산 방식   : 트레일링 스탑")
        print(f"  트레일 비율 : 고점 대비 -{int(TRAILING_STOP_RATE * 100)}%")
        print(f"  활성화 기준 : +{int(TRAILING_STOP_ACTIVATE_RATE * 100)}% 이상 상승 시")
        print(f"  하드 손절   : -{int(LOSS_RATE * 100)}%")
    else:
        print(f"  익절 기준   : +{int(PROFIT_RATE * 100)}%")
        print(f"  손절 기준   : -{int(LOSS_RATE * 100)}%")
    print(f"  강제 청산   : {FORCE_SELL_TIME}")
    print(f"  체크 주기   : {CHECK_INTERVAL}초")
    print(f"  최대 매매   : {MAX_TRADES_PER_DAY}회/일")
    print(f"  돌파여유선  : {MAX_BREAKOUT_GAP}% 이내")
    print(f"  스크리닝 풀 : 코스피200 상위 {KOSPI_POOL_SIZE}개 + 관심종목")
    print("=" * 40)
